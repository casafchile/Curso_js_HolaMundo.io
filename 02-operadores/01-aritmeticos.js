//Incremento y Decremento
// Incremento:
console.log(a++); // Post-incremento (a se incrementa después de ser usado)
console.log(++a); // Pre-incremento (a se incrementa antes de ser usado)
console.log(a); // Muestra el valor de a después del incremento
// Decremento:
console.log(a--); // Post-decremento (a se decrementa después de ser usado)
console.log(--a); // Pre-decremento (a se decrementa antes de ser usado)
console.log(a); // Muestra el valor de a después del decremento